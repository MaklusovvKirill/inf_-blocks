a = int(input("Введите число a: "))
b = int(input("Введите число b: "))
c = int(input("Введите число c: "))

if a < b < c:
    print("Первое равенство правильное: ")
else:
    print("Первое равенство неверно")


if b > a > c:
    print("Второе равенство равно")
else:
    print("Второе равенство неверно")
